    private void adjustImage(){
        ImageIcon originalImage = new ImageIcon(getClass().getResource("/images/searching.gif"));
        Image newImage = originalImage.getImage().getScaledInstance(
                this.lblSearchImage.getWidth(), this.lblSearchImage.getHeight(), 
                Image.SCALE_DEFAULT);
        Icon newIcon = new ImageIcon(newImage);
        this.lblSearchImage.setIcon(newIcon);
    }

